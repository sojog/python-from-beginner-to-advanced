
## Primitives
x = 10
print(x)
print(type(x))

x = 10.7
print(x)
print(type(x))

x = "10.32"
print(x)
print(type(x))

x = False
print(x)
print(type(x))

## Lists
x = [10, 20, 30, "Hello", True, [39, 20]]
print(x)
print(type(x))

x.pop()
print(x)

x = (10, 20, 30, "Hello", True, [39, 20])
print(x)
print(type(x))

x = (10, 20, 30, "Hello", True)
print(x)

x = {"key1":"value", 10:30, (100, 200): 20}
print(x)
print(type(x))